local quickrun = require('utils.quickrun')
local flip_word = require('utils.flip-word')
local swap = require('utils.treesitter.swap')
local select = require('utils.treesitter.select')
require('utils.stl').setup()
require('utils.terminal')

vim.keymap.set('n', 'ts', swap.act)
vim.keymap.set({ 'x', 'o' }, 'if', function()
  select.act('function', false)
end)
vim.keymap.set({ 'x', 'o' }, 'af', function()
  select.act('function', true)
end)
vim.keymap.set({ 'x', 'o' }, 'ic', function()
  select.act('class', false)
end)
vim.keymap.set({ 'x', 'o' }, 'ac', function()
  select.act('class', true)
end)
vim.keymap.set({ 'x', 'o' }, 'il', function()
  select.act('loop', false)
end)
vim.keymap.set({ 'x', 'o' }, 'al', function()
  select.act('loop', true)
end)

vim.keymap.set('n', '<leader>r', quickrun.run)
vim.keymap.set('n', 'ta', flip_word.toggle)

vim.keymap.set({ 'n', 't' }, '<A-i>', function()
  _G.Terms.toggle({ pos = 'float', id = 'Float Term' })
end)
vim.keymap.set({ 'n', 't' }, '<C-x>[', function()
  _G.Terms.toggle({ pos = 'vsp', id = 'SP Term' })
end)
vim.keymap.set({ 'n', 't' }, '<C-x>]', function()
  _G.Terms.toggle({ pos = 'sp', id = 'VSP Term' })
end)
vim.keymap.set({ 'n', 't' }, '<C-x>h', function()
  _G.Terms.hide()
end)
vim.keymap.set({ 'n', 't' }, '<C-x>H', function()
  _G.Terms.hideall()
end)
vim.keymap.set({ 'n', 't' }, '<C-x>l', function()
  _G.Terms.resume()
end)
vim.keymap.set({ 'n', 't' }, '<C-x>L', function()
  _G.Terms.resumeall()
end)

-- Add underline style for link.
vim.fn.matchadd('Underlined', [[\v(https|http|ftp|git|ssh):\/\/[a-zA-Z0-9._~!*'();:@&=+$,/?#%-]+]])
